    /**
     * Handle the Post "force deleted" event.
     */
    public function forceDeleted(Post $post): void
    {
        //
    }

    private function checkPublished(Post $post): void
    {
        if ($post->isDirty('published') && $post->published) {
            $post->published_at = \Carbon\Carbon::now();
        }
    }
}
